# STRING (str) → texto
nombre = "Pedro"  # guarda un texto
mensaje = "Hola mundo"  # otro ejemplo de texto

# INTEGER (int) → número entero (sin decimales)
edad = 25  # número entero
cantidad = 100  # otro entero

# FLOAT (float) → número decimal
precio = 19.99  # número con decimales
altura = 1.75  # otro decimal

# BOOLEAN (bool) → verdadero o falso
es_estudiante = True  # valor verdadero
tiene_trabajo = False  # valor falso

# USO DE LAS VARIABLES
print(nombre)        # imprime texto
print(edad)          # imprime entero
print(precio)        # imprime decimal
print(es_estudiante) # imprime True o False

# COMBINANDO DATOS (IMPORTANTE)
# Para unir texto con números, usamos str()
print("Edad: " + str(edad))  # convierte el número a texto